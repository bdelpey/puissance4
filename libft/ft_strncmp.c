#include	<string.h>

int			ft_strncmp(const char *s1, const char *s2, size_t n)
{
	while (*s1 == *s2 && n - 1 > 0)
	{
		n--;
		s1++;
		s2++;
		if (*s1 == '\0')
			return (0);
	}
	if (*s1 > *s2)
		return (1);
	else if (*s1 == *s2 && n == 1)
		return (0);
	else
		return (-1);
}
